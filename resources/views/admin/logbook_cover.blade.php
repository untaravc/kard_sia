        KODE ETIK PROFESIONAL PESERTA PPDS
    </div>
    <p>
        Sebagai peserta PPDS-1 (Program Pendidikan Dokter Spesialis-1) Ilmu Penyakit
        Jantung dan Pembuluh Darah saya memahami bahwa saya memiliki banyak keuntungan
        dan kehormatan dalam mempelajari ilmu kedokteran. Selama pendidikan, saya menyadari
        besarnya tanggung jawab terhadap kesehatan seseorang. Keadaan tersebut memerlukan
        standar etika dan empati yang tinggi. Dalam mengikuti semua aktivitas pendidikan saya
        akan berpegang teguh pada Kode Etik Kedokteran Indonesia. Lebih spesifik, saya
        senantiasa akan menerapkan prinsip-prinsip yang tertulis di bawah ini dalam pendidikan
        akademik, klinik, dan penelitian. Saya akan berusaha untuk mempertahankan semangat
        dan mematuhi kode ini selama pendidikan saya dan sepanjang karir dokter saya.
    </p>
    <div>Kejujuran</div>
    <ol>
        <li>Saya akan senantiasa berupaya mempertahankan standar kejujuran yang tinggi.</li>
        <li>Saya akan bersikap jujur terhadap pasien dan akan melaporkan secara akurat
            riwayat penyakit pasien, penemuan fisik, hasil pemeriksaan laboratorium,
            pemeriksaan penunjang lain dan informasi lain dalam pelayanan kesehatan.
        </li>
        <li>Dalam melakukan penelitian saya akan bersikap tanpa bias (obyektif), melaporkan
            hasilnya dengan jujur, memberikan penghargaan terhadap ide penelitian yang
            dilakukan oleh orang lain dan menghormati obyek penelitian sesuai dengan etika
            penelitian.
        </li>
    </ol>
    <div>Kerahasiaan</div>
    <ol>
        <li>Saya akan menjaga kerahasiaan sebagai kewajiban utama dalam penyelenggaraan
            pelayanan pasien.
        </li>
        <li>Saya tidak akan membahas pasien dengan anggota tim penyelenggara kesehatan
            di depan umum (tidak di dalam lift, lorong, kantin).
        </li>
        <li>Saya akan bersikap jujur terhadap pasien dan akan melaporkan secara akurat
            riwayat pasien, temuan tanda fisik, hasil tes dan informasi lain pelayanan
            kesehatan pasien, sesuai dengan keinginan pasien.
        </li>
    </ol>
    <div>
        Penghargaan terhadap individu lain
    </div>

    <ol>
        <li>Saya akan berupaya mempertahankan suasana yang kondusif dalam memberi
            pelayanan kepada pasien dan pembelajaran terhadap pasien.
        </li>
        <li>Saya akan memperlakukan pasien dan keluarga pasien dengan hormat, baik pada
            saat berhadapan langsung dengan pasien dan keluarganya, atau saat pembahasan
            antar anggota tim pelayanan kesehatan.
        </li>
        <li>Saat berinteraksi dengan pasien, saya akan menjaga dan menghargai privasi
            mereka.
        </li>
        <li>Saat berinteraksi dengan anggota tim kesehatan, saya akan bersikap kooperatif.</li>
        <li>Saya tidak mentoleransi diskriminasi ras, agama, jenis kelamin, orientasi seksual,
            umur, kecacatan atau status sosial dalam proses pelayanan pasien.
        </li>
        <li>Saya akan menilai kolega secara adil dan berusaha menyelesaikan semua konflik,
            dan memperlakukan secara baik setiap orang yang terlibat.
        </li>
        <li>Saya akan selalu menghormati guru-guru saya.</li>
    </ol>
    <div>Tanggung jawab</div>
    <ol>
        <li>Saya akan menempatkan pasien sebagai prioritas utama dalam tugas klinik saya.</li>
        <li>Saya akan mengenali keterbatasan dan meminta konsultasi/merujuk bila tingkat
            pengalaman saya tidak cukup untuk mengatasi sendiri.
        </li>
        <li>Saya akan bersikap profesional dalam tingkah laku, berbicara dan berpenampilan
